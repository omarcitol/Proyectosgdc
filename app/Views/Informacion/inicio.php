    <h1>Sistema de Gestión de Calificaciones</h1>
    <p>El sistema de gestión de calificaciones para la Universidad Experimental Marítima del Caribe es una herramienta vital para el seguimiento y control del rendimiento académico de los estudiantes.</p>
    <p>A través de este sistema, los profesores pueden ingresar las calificaciones de sus alumnos de manera rápida y sencilla, facilitando así la comunicación de los resultados y el monitoreo de los avances de cada estudiante.</p> 
    <p>La importancia de contar con un sistema de gestión de calificaciones radica en la transparencia y eficiencia en la comunicación de los resultados académicos.</p>
    <p>Los estudiantes pueden acceder a sus calificaciones de forma inmediata, lo que les permite identificar áreas de oportunidad y tomar medidas para mejorar su desempeño.</p>

    <div class="instrucciones">
      <h2>Instrucciones para acceder al sistema de gestión de calificaciones:</h2>
      <ol>
        <li>Ingresa al sitio web de la Universidad Experimental Marítima del Caribe.</li>
        <li>Busca el apartado de "Consulta de notas" o "Calificaciones".</li>
        <li>Selecciona la opción de "Iniciar sesión" e ingresa tu usuario y contraseña proporcionados por la universidad.</li>
        <li>Una vez dentro del sistema, podrás acceder a tus calificaciones y el historial académico.</li>
        <li>Si tienes alguna duda o problema para acceder a tus calificaciones, contacta al departamento de atención al estudiante en la UMC o a tu profesor con respecto a algun error en tus notas.</li>
      </ol>
